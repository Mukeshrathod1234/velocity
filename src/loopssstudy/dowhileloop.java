package loopssstudy;

public class dowhileloop {

	public static void main(String[] args) {
		
		
		
		int a=10;
		
		do {
			System.out.print(" "+a);
			
			a=a+10;
		}
		
		while (a<=100);

		System.out.println(" ");
		System.out.println("==========================");
		
		int b=110;
		
		do {
			System.out.print(" "+b);
			
			b=b-11;
			
		} while (b>=11);
	}
  
 

}
