package loopssstudy;

public class forloop {

	public static void main(String[] args) {
		
	
		
		
		for(int a=4; a<=40;a=a+4) {
			
			System.out.print(" "+a);
		}
		System.out.println(" ");
		System.out.println("========================");
		for(int a=50; a>=5;a=a-5) {
			
			System.out.print(" "+a);
		}
	}
}
	