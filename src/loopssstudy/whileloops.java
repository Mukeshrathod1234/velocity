package loopssstudy;

public class whileloops {

	public static void main(String[] args) {
		

		
		int a=7;
		while (a<=70)
		{
			System.out.print(" "+a);
			
			a=a+7;
		}
		System.out.println(" ");
		System.out.println("=============================");
		
		
		int b=70;
		
		while (b>=7)
		
	{
			System.out.print(" "+b);
			
			b=b-7;
			
		}
		
		
	}
     
}
